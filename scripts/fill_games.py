async def fill():
    async with async_session() as session:
        # Clear existing
        await session.execute(delete(TruthOrDare))
        await session.execute(delete(WordGuessGame))
        await session.commit()
        
        # Add Truth questions
        truth_count = 0
        for category, questions in TRUTH_QUESTIONS.items():
            for text, difficulty in questions:
                session.add(TruthOrDare(
                    type="truth",
                    category=category,
                    text=text,
                    difficulty=difficulty
                ))
                truth_count += 1
        
        # Add Dare actions
        dare_count = 0
        for category, actions in DARE_ACTIONS.items():
            for text, difficulty in actions:
                session.add(TruthOrDare(
                    type="dare",
                    category=category,
                    text=text,
                    difficulty=difficulty
                ))
                dare_count += 1
        
        # Add Word Guess words
        for word, category in WORD_GUESS_WORDS:
            session.add(WordGuessGame(word=word, category=category))
        
        await session.commit()
        print(f"Added {truth_count} truth questions and {dare_count} dare actions.")
        print(f"Added {len(WORD_GUESS_WORDS)} words for guessing game.")

if __name__ == "__main__":
    asyncio.run(fill())
